int	ft_is_prime(int nb)
{
	long long	i;

	if (nb <= 1)
		return (0);
	else if (nb <= 3)
		return (1);
	i = 2;
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
