int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (1)
	{
		if (str[i] == '\0')
			break ;
		if (!('a' <= str[i] && str[i] <= 'z'))
			if (!('A' <= str[i] && str[i] <= 'Z'))
				return (0);
		i++;
	}
	return (1);
}
